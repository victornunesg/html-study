print('--------------------------------')
print('Basic Arithmetic Operations')
print('--------------------------------')

a = 6
b = 2
print('Addition : ', a + b)
print('Subtraction : ', a - b)
print('Multiplication : ', a * b)
print('Division (float) : ', a / b)
print('Division (floor) : ', a // b)  # it will return 1 in a 3/2 division, for example
print('Modulus : ', a % b)  # left over, rest of the division
print('Exponent : ', a ** b)
print('')
